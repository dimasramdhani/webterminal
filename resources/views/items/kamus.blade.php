<center><H3> ATOMIZER </H3>  </a>
<BR>
Atomizer adalah satu komponen Vaporizer yang berfungsi sebagai tempatmengubah cairan <br>
liquid menjadi uap sehingga bisa kita hisap ( penguapan ). <br>
atomizer terdiri atas rangkaian koil, kapas Vapor dan cairan Liquid <br> 
</center>

<br>
<br>
<center><H2> RDA </H2><center>
<BR>
RDA adalahsingkatan dari Rebuildable Driping Atomizer yang mana cara kerjanya yaitu <br>
atomizer menguapkan Liquid yang di tetes atau di drip pada kapas Vapor dan Koil. <br>
jadi untuk jenis atomizer ini tidak memiliki media Tank sebagai penampung cairan liquid <br>
cara kerjanya, cairan Liquid wajib di teteskan dahulu pada kapas sebagai media penampung <br>
baru kemudian di hisap. <br> 
<br>
<br>
<br>
<center><H2> RTA </H2><center>
<BR>
RTA adalah singkatan dari Rebuildable Tank Atomizer, Atomizer ini memiliki konsep <br>
Tank sebagai wadah penampung Liquid. Namun RTA ini lebih simpel dari RDA karena <br>
tidak perlu meneteskan liquid setiap sebelum vaping .
<br>
<br>
<br>
<center><H2> RDTA </H2><center>
<BR>
RDTA  adalah singkatan dari Rebuildable Driping Tank Atomizer, Atomizer ini adalah <br>
gabungan dari konsep RDA dan RTA. RDTA diklaim sebagai jenis atomizer yang fleksibel <br>
karena memiliki kedua fungsi dari RDA dan RTA .
<br>
<br>
<br>
<center><H2> AIR HOLE </H2><center>
<BR>
AIR HOLE adalah lubang yang ada pada atomizer yang memiliki fungsi sebagai akses <br>
 udara masuk pada saat Vape di hisap.
