<center><H2> TERMINALL </H2><center>
<BR>
MOD elektrikal adalah suatu komponen vape sebagai mesin utama yang menggunakan <br>
baterai agar bisa memanaskan koil sehingga terjadi penguapan liquid menjadi uap vapor .<br>
MOD elektrikal ini berbeda dengan jenis MOD lain, perbedaan paling terlihat adalah <br>
dalam segi tampilan, mod elektrikal memiliki display berupa pilihan mode tenaga yang <br>
di keluarkan, MOD Elektrikal ini dapat di sesuaikan powernya melalui pengaturan display<br>
yang ada. (mengatur Watt) . 
<br>
<br>
<br>
<center><H2> MOD MECHANICAL </H2><center>
<BR>
MOD mechanical adalah komponen utama dalam vape, yang pada umumnya berbentuk panjang <br>
seperti sepidol papan tulis. MOD jenis ini tidak memiliki display untuk mengatur watt <br>
dehingga tenaga yang di keluarkan selalu mengikuti kualitas dan kapasitas baterai yang <br>
digunakan. dan MOD jenis ini hanya di rekomendasikan bagi pengguna Vaporizer yang sudah <br>
faham tentang pengaturan Hukum OHM. karena dalam penggunaanya cukup beresiko ketika kita <br>

<br>
<br>
<br>
<center><H2> PODS </H2><center>
<BR>
PODS adalah sebuah MOD vape yang bentuknya cenderung kecil, dan tidak memiliki atomizer <br>
melainkan biasanya PODS ini menggunakan Catridge (konsepnya sama seperti RTA). banyak yang <br>
menggunakan alat PODS ini sebagai Secondary Vape jika sedang bosan dengan vape jenis biasa
<br>
